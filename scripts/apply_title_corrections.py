def main():
    # 매핑 로드
    with open(MAPPINGS_PATH, 'r', encoding='utf-8') as f:
        mappings = json.load(f)

    print(f"📚 현재 매핑: {len(mappings)}개")
    print(f"🔄 수정 항목: {len(CORRECTIONS)}개")

    # 매핑 업데이트
    updated = 0
    for jp, kr in CORRECTIONS.items():
        if jp in mappings:
            if mappings[jp] != kr:
                mappings[jp] = kr
                updated += 1
        else:
            mappings[jp] = kr
            updated += 1

    # 정렬 후 저장
    sorted_m = dict(sorted(mappings.items()))
    with open(MAPPINGS_PATH, 'w', encoding='utf-8') as f:
        json.dump(sorted_m, f, ensure_ascii=False, indent=2)
    print(f"💾 매핑 저장: {len(sorted_m)}개 (수정: {updated}개)")

    # DB 업데이트
    if not DB_URL:
        print("⚠️ DB URL 없음")
        return

    conn = psycopg2.connect(DB_URL)
    cur = conn.cursor()
    w_total = r_total = 0

    for jp, kr in CORRECTIONS.items():
        if not kr:
            continue
        # works 업데이트 (빈값이거나 혼합 문자열 교체)
        cur.execute("""
            UPDATE works SET title_kr = %s
            WHERE title = %s
        """, (kr, jp))
        w_total += cur.rowcount

        # rankings 업데이트
        cur.execute("""
            UPDATE rankings SET title_kr = %s
            WHERE title = %s
        """, (kr, jp))
        r_total += cur.rowcount

    conn.commit()
    conn.close()
    print(f"💾 DB: works {w_total}행, rankings {r_total}행 업데이트")


if __name__ == "__main__":
    main()
